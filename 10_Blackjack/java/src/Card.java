public class Card {
    private int value;
    private String suit;

    public void setValue(int value) {
        this.value = value;
    }

    public int getValue() {
        return this.value;
    } 
    
    public void setSuit(int suit) {
        this.suit = suit;
    }

    public int getSuit() {
        return this.suit;
    }

}